// GENERATED from tarkov.dev's GraphQL API — `node _dev/build_mapmarkers.js`.
// Don't hand-edit: regenerate, and diff. Coordinates are game (x, y, z) and go
// through the same mapPoint()/floorOf() in mapdata.js that the quest pins use,
// so anything wrong here is wrong the same way for both.
//
// ── ON "GUARANTEED" SPAWNS ────────────────────────────────────────────────────
// There is NO spawn chance, weight or "guaranteed" flag anywhere in Tarkov's
// public data. `lootLoose` gives a position and the list of items that CAN
// appear there — nothing more. So this file cannot, and does not, claim any item
// always spawns anywhere.
//
// What the data does support is EXCLUSIVITY: how many other items compete for
// the same exact spot. `alts` is that number.
//   alts === 0  -> nothing else can spawn at that point (the strongest claim
//                  the data allows, and the closest honest thing to "reliable")
//   alts 1..4   -> a fixed spot shared with 1 to 4 other items
// Positions listing 6 or more possible items are dropped: at that dilution a marker says
// little beyond "loot exists here". The marked rooms (`mk`) are kept separately
// precisely because they are the opposite case — dozens to hundreds of items in
// one pool — and their pool size is carried so the UI can say so out loud.
//
// Worked example, and the reason this warning exists: the Machinery Key has
// exactly ONE loose position on Customs, and that position lists 131 possible
// items — it is the Dorms marked room. "The machinery key always spawns in
// dorms" is therefore false as stated; the marked room is where it *can* roll.
//
