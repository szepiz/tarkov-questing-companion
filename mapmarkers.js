//   ex  [x, y, z, faction, needsSwitch, name]        faction 0=pmc 1=scav 2=shared
//   hz  [x, y, z, type]                              type    0=minefield 1=sniper 2=other
//   lk  [x, y, z, type, keyShort, keyName]           type    0=door 1=trunk 2=container 3=switch
//   lt  [x, y, z, cat, alts, itemName]               cat     0=keys 1=intel 2=valuables
//                                                            3=medical 4=electronics 5=drink
//   mk  [x, y, z, poolSize, keysInPool]              keysInPool = key names in that pool, "" if none
//
// Hazards carry no polygon outline upstream (every `outline` is empty), so they
// are points. The dense clouds — Lighthouse 344, Streets 197 — trace the mined
// borders on their own; don't write code expecting polygons.
//
